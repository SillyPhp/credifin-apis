    public $variables = [];
    private $__pCost = '';
    private $__pSalt = '';
    private $__pAlgo = '';

    private function __password_encrypt_keys()
    {
        $this->__pCost = Yii::$app->params->p_cost;
        $this->__pSalt = Yii::$app->params->p_salt;
        $this->__pAlgo = Yii::$app->params->p_algo;
    }

    public function encrypt()
    {
        return $this->__encrypt();
    }

    public function encrypt_pass()
    {
        return $this->__encrypt_password();
    }

    public function verify_pass()
    {
        return $this->__verify_password();
    }

    public function unsetFlashMessages()
    {
        foreach (Yii::$app->session->getAllFlashes() as $key => $message) {
            Yii::$app->session->setFlash($key, null);
        }
    }

    public function create_slug()
    {
        $this->variables['slug'] = $this->__create_slug();
        $count = (new \yii\db\Query())
            ->from($this->variables['table_name'])
            ->where(['LIKE', $this->variables['field_name'], $this->variables['slug'] . '%', false])
            ->count();
        if ($count > 0) {
            return $this->variables['slug'] . '-' . $count;
        } else {
            return $this->variables['slug'];
        }
    }

    private function __encrypt()
    {
        $model = new \Hashids\Hashids(Yii::$app->params->encryption->salt, Yii::$app->params->encryption->min_length, Yii::$app->params->encryption->alphabet);
        return $model->encode($this->variables['string']);
    }

    private function __encrypt_password()
    {
        $this->__password_encrypt_keys();
        $model = new PasswordEncrypt();
        return $model->password_hash($this->variables['password'], PASSWORD_BCRYPT, ['cost' => $this->__pCost, 'salt' => $this->__pSalt]);
    }

    private function __verify_password()
    {
        $this->__password_encrypt_keys();
        $model = new PasswordEncrypt();
        return $model->password_verify($this->variables['password'], $this->variables['hash']);
    }

